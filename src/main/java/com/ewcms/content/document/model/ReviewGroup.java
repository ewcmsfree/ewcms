package com.ewcms.content.document.model;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.SequenceGenerator;
import javax.persistence.Table;

/**
 * 审核用户组
 * 
 * @author wu_zhijun
 * 
 */
@Entity
@Table(name = "doc_reviewgroup")
@SequenceGenerator(name = "seq_doc_reviewgroup", sequenceName = "seq_doc_reviewgroup_id", allocationSize = 1)
public class ReviewGroup implements Serializable {

	private static final long serialVersionUID = 5703561985089566858L;

	@Id
    @GeneratedValue(generator = "seq_doc_reviewgroup",strategy = GenerationType.SEQUENCE)
	@Column(name = "id")
	private Long id;
	@Column(name = "groupName")
	private String groupName;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getGroupName() {
		return groupName;
	}

	public void setGroupName(String groupName) {
		this.groupName = groupName;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		ReviewGroup other = (ReviewGroup) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}
}
